void matrix_init_user(void) {
    autocorrect_enabled = !autocorrect_enabled;
    autocorrect_toggle();
}

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        #ifdef AUDIO_ENABLE
        case DF(_QWERTY):
            if (record->event.pressed) {
                if (biton32(default_layer_state) != _QWERTY) {
                    PLAY_SONG(qwerty_song);
                }
            }
            return true;
        case DF(_COLEMAKDH):
            if (record->event.pressed) {
                if (biton32(default_layer_state) != _COLEMAKDH) {
                    PLAY_SONG(colemak_song);
                }
            }
            return true;
        case USER_FANTASIE_IMPROMPTU:
            if (record->event.pressed) {
                PLAY_SONG(fantasie_impromptu);
            }
            return false;
        case FF_PRELUDE_KEY:
            if (record->event.pressed) {
                PLAY_SONG(ff_prelude);
            }
            return false;
        case IMPERIAL_MARCH_KEY:
            if (record->event.pressed) {
                PLAY_SONG(imperial_march);
            }
            return false;
        case COIN_SOUND_KEY:
            if (record->event.pressed) {
                PLAY_SONG(coin_sound);
            }
            return false;
        case ONE_UP_SOUND_KEY:
            if (record->event.pressed) {
                PLAY_SONG(one_up_sound);
            }
            return false;
        case ZELDA_PUZZLE_KEY:
            if (record->event.pressed) {
                PLAY_SONG(zelda_puzzle);
            }
            return false;
        case MARIO_MUSHROOM_KEY:
            if (record->event.pressed) {
                PLAY_SONG(mario_mushroom);
            }
            return false;

        #endif // AUDIO_ENABLE

        case AC_TOGG_CUSTOM:
            if (record->event.pressed) {
                autocorrect_enabled = !autocorrect_enabled;
                autocorrect_toggle();
            }
            return false;

        /* KEYBOARD PET STATUS START */

        case KC_LCTL:
        case KC_RCTL:
            if (record->event.pressed) {
                isSneaking = true;
            } else {
                isSneaking = false;
            }
            break;
        case KC_SPC:
            if (record->event.pressed) {
                isJumping  = true;
                showedJump = false;
            } else {
                isJumping = false;
            }
            break;

        /* KEYBOARD PET STATUS END */
    }
    return true;
}
